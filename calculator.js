const first=10
const sec=6
const op='*'


if(op=='+'){
  console.log(`${first} + ${sec} = ${first+sec}`)
}
else if(op=='-'){
  console.log(`${first} - ${sec} = ${first-sec}`)
}
else if(op=='*'){
  console.log(`${first} * ${sec} = ${first*sec}`)
}
else if(op=='/'){
  console.log(`${first} / ${sec} = ${first/sec}`)
}